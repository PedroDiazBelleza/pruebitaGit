
package modelo;

public class Platos {
    int cod_plato;
    String nombre_plato;
    String descripcion_plato;
    Double precio_plato;
    int cod_categoria;
    String caracter_plato;

    public int getCod_plato() {
        return cod_plato;
    }

    public void setCod_plato(int cod_plato) {
        this.cod_plato = cod_plato;
    }

    public String getNombre_plato() {
        return nombre_plato;
    }

    public void setNombre_plato(String nombre_plato) {
        this.nombre_plato = nombre_plato;
    }

    public String getDescripcion_plato() {
        return descripcion_plato;
    }

    public void setDescripcion_plato(String descripcion_plato) {
        this.descripcion_plato = descripcion_plato;
    }

    public Double getPrecio_plato() {
        return precio_plato;
    }

    public void setPrecio_plato(Double precio_plato) {
        this.precio_plato = precio_plato;
    }

    public int getCod_categoria() {
        return cod_categoria;
    }

    public void setCod_categoria(int cod_categoria) {
        this.cod_categoria = cod_categoria;
    }

    public String getCaracter_plato() {
        return caracter_plato;
    }

    public void setCaracter_plato(String caracter_plato) {
        this.caracter_plato = caracter_plato;
    }
    
}

